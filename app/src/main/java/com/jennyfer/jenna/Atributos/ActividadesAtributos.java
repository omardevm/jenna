package com.jennyfer.jenna.Atributos;

public class ActividadesAtributos {

    private int estadoA;
    private String idA;
    private String titulo;
    private String descripcion;
    private String horaA;
    private String fechaA;
    private String grupo;
    private String division;
    private String autor;
    private String fechaPublicacion;
    private String archivo;


    public ActividadesAtributos (){}

    public int getEstadoA() { return estadoA; }
    public void setEstadoA(int estadoA) { this.estadoA = estadoA; }

    public String getIdA() { return idA; }
    public void setIdA(String idA) { this.idA = idA; }

    public String getTitulo() { return titulo; }
    public void setTitulo(String titulo) { this.titulo = titulo; }

    public String getDescripcion() { return descripcion; }
    public void setDescripcion(String descripcion) { this.descripcion = descripcion; }

    public String getHoraA() { return horaA; }
    public void setHoraA(String horaA) { this.horaA = horaA; }

    public String getFechaA() { return fechaA; }
    public void setFechaA(String fechaA) { this.fechaA = fechaA; }

    public String getGrupo() { return grupo; }
    public void setGrupo(String grupo) { this.grupo = grupo; }

    public String getDivision() { return division; }
    public void setDivision(String division) { this.division = division; }

    public String getAutor() { return autor; }
    public void setAutor(String autor) { this.autor = autor; }

    public String getFechaPublicacion() { return fechaPublicacion; }
    public void setFechaPublicacion(String fechaPublicacion) { this.fechaPublicacion = fechaPublicacion; }

    public String getArchivo() { return archivo; }
    public void setArchivo(String archivo) { this.archivo = archivo; }
}
