package com.jennyfer.jenna.Atributos;


public class PerfilAtributos  {

    private String usuario;
    private String nombreUser;
    private String paternoUser;
    private String maternoUser;
    private String divisionUSer;
    private String fechaUSer;
    private String semestreUser;
    private String correo;
    private String tipouser;
    private String perfilito;

    public PerfilAtributos(){}


    public String getNombreUser() {
        return nombreUser;
    }
    public void setNombreUser(String nombreUser) {
        this.nombreUser = nombreUser;
    }

    public String getPaternoUser() {
        return paternoUser;
    }
    public void setPaternoUser(String paternoUser) {
        this.paternoUser = paternoUser;
    }

    public String getMaternoUser() {
        return maternoUser;
    }
    public void setMaternoUser(String maternoUser) {
        this.maternoUser = maternoUser;
    }

    public String getDivisionUSer() {
        return divisionUSer;
    }
    public void setDivisionUSer(String divisionUSer) {
        this.divisionUSer = divisionUSer;
    }

    public String getFechaUSer() {
        return fechaUSer;
    }
    public void setFechaUSer(String fechaUSer) {
        this.fechaUSer = fechaUSer;
    }

    public String getSemestreUser() {
        return semestreUser;
    }
    public void setSemestreUser(String semestreUser) {
        this.semestreUser = semestreUser;
    }

    public String getCorreo() {
        return correo;
    }
    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getTipouser() {
        return tipouser;
    }
    public void setTipouser(String tipouser) {
        this.tipouser = tipouser;
    }

    public String getUsuario() { return usuario; }
    public void setUsuario(String usuario) { this.usuario = usuario; }

    public String getPerfilito() { return perfilito; }
    public void setPerfilito(String perfilito) { this.perfilito = perfilito; }
}
